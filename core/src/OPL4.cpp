    static uint16_t resolveWaveNumber(uint8_t ws, uint8_t note) {
        const RegionRange* range;
        if (ws < 128) {
            range = &kGmRegionIndex[ws];
        } else {
            range = &kDrumRegionRange;
            // ドラムは note が GM標準ドラムノート番号と一致する前提
        }
        if (range->count == 0) return 0;
        for (uint16_t i = 0; i < range->count; ++i) {
            const WaveRegion& r = kAllRegions[range->offset + i];
            if (note >= r.keyMin && note <= r.keyMax) return r.tone;
        }
        // 該当キーレンジが見つからない場合は最初のリージョンにフォールバック
        return kAllRegions[range->offset].tone;
    }
